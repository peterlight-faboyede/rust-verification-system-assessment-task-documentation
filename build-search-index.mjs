/**
 * Run: node build-search-index.mjs
 * Generates search-index.json from chapter HTML files (no extra deps).
 */
import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const __dirname = dirname(fileURLToPath(import.meta.url));

const FILES = [
  "application-stack.html",
  "architecture.html",
  "configuration.html",
  "devops.html",
  "execution.html",
  "performance-decisions.html",
  "security.html",
  "testing.html",
  "tracing-and-observability.html",
];

function stripTags(html) {
  return html
    .replace(/<script[\s\S]*?<\/script>/gi, " ")
    .replace(/<style[\s\S]*?<\/style>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/\s+/g, " ")
    .trim();
}

function innerText(html, start, maxLen = 900) {
  const slice = html.slice(start, start + maxLen * 2);
  const t = stripTags(slice);
  return t.length > maxLen ? `${t.slice(0, maxLen)}…` : t;
}

function extractMain(html) {
  const m = html.match(/<main[^>]*>([\s\S]*)<\/main>/i);
  return m ? m[1] : "";
}

function pageTitle(html) {
  const m = html.match(/<title>([^<]+)<\/title>/i);
  if (!m) return "Documentation";
  return m[1].replace(/\s*-\s*Technical documentation\s*$/i, "").trim();
}

function buildFileIndex(filename, html) {
  const main = extractMain(html);
  const pTitle = pageTitle(html);
  const items = [];

  const tagRe =
    /<(section|article|h2|h3|h4)([^>]*)\sid="([^"]+)"([^>]*)>([\s\S]*?)<\/\1>/gi;
  let m;
  let sectionId = "";
  let sectionTitle = "";

  const walk = main;
  let pos = 0;
  const openings = [];

  const openRe = /<(section|article|h2|h3|h4)[^>]*\sid="([^"]+)"[^>]*>/gi;
  while ((m = openRe.exec(walk)) !== null) {
    const tag = m[1].toLowerCase();
    if (!["section", "article", "h2", "h3", "h4"].includes(tag)) continue;
    openings.push({
      tag,
      id: m[2],
      index: m.index,
      fullMatchLen: m[0].length,
    });
  }

  openings.sort((a, b) => a.index - b.index);

  for (let i = 0; i < openings.length; i++) {
    const o = openings[i];
    const next = openings[i + 1];
    const start = o.index + o.fullMatchLen;
    const end = next ? next.index : walk.length;
    const blockHtml = walk.slice(start, end);

    if (o.tag === "section") {
      sectionId = o.id;
      const h2m = blockHtml.match(/<h2[^>]*>([\s\S]*?)<\/h2>/i);
      sectionTitle = h2m ? stripTags(h2m[1]).slice(0, 200) : o.id;
    }

    let subsectionId = "";
    let subsectionTitle = "";
    if (o.tag === "h3" || o.tag === "h4") {
      subsectionId = o.id;
      const htm = walk.slice(o.index, o.index + 400);
      const titleM = htm.match(new RegExp(`<${o.tag}[^>]*>([\\s\\S]*?)<\\/${o.tag}>`, "i"));
      subsectionTitle = titleM ? stripTags(titleM[1]).slice(0, 200) : o.id;
    }

    const snippet = innerText(blockHtml, 0, 700);
    const headingLevel =
      o.tag === "section" ? 2 : o.tag === "article" ? 3 : parseInt(o.tag.slice(1), 10);
    const displayHeading =
      o.tag === "section" ? sectionTitle : subsectionTitle || o.id;

    items.push({
      id: `${filename}::${o.id}`,
      page: filename,
      pageTitle: pTitle,
      sectionId: o.tag === "section" ? o.id : sectionId,
      sectionTitle: o.tag === "section" ? sectionTitle : sectionTitle || "",
      subsectionId: o.tag === "section" ? "" : subsectionId,
      subsectionTitle: o.tag === "section" ? "" : subsectionTitle,
      headingTag: o.tag,
      headingLevel,
      anchor: `${filename}#${o.id}`,
      snippet,
      searchText:
        `${pTitle} ${sectionTitle} ${subsectionTitle} ${displayHeading} ${snippet}`.toLowerCase(),
    });
  }

  return items;
}

const all = [];
for (const f of FILES) {
  const path = join(__dirname, f);
  let html;
  try {
    html = readFileSync(path, "utf8");
  } catch {
    console.warn("skip", f);
    continue;
  }
  all.push(...buildFileIndex(f, html));
}

const out = { generated: new Date().toISOString(), items: all };
const compact = JSON.stringify(out);
writeFileSync(join(__dirname, "search-index.json"), compact, "utf8");
writeFileSync(
  join(__dirname, "search-index.js"),
  `/** Generated by build-search-index.mjs — do not edit */\nwindow.__DOC_SEARCH_INDEX__=${compact};\n`,
  "utf8",
);
console.log("Wrote search-index.json and search-index.js with", all.length, "entries");
